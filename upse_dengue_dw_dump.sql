CREATE VIEW `kpi_ocupacion_hospitalaria` AS select `di`.`canton` AS `canton`,`di`.`anio` AS `anio`,`di`.`semana_epidem` AS `semana_epidem`,`di`.`camas_totales` AS `camas_totales`,`di`.`camas_ocupadas` AS `camas_ocupadas`,round(((`di`.`camas_ocupadas` / `di`.`camas_totales`) * 100),2) AS `tasa_ocupacion_pct` from `dim_infraestructura` `di`;

DROP VIEW IF EXISTS kpi_tiempos_espera;
CREATE VIEW `kpi_tiempos_espera` AS select `dg`.`canton` AS `canton`,`dt`.`anio` AS `anio`,`dt`.`semana_epidem` AS `semana_epidem`,round(`fi`.`espera_promedio_h`,2) AS `tiempo_espera_promedio_horas` from ((`fact_incidencia` `fi` join `dim_geografia` `dg` on((`fi`.`id_geografia` = `dg`.`id_geografia`))) join `dim_tiempo` `dt` on((`fi`.`id_tiempo` = `dt`.`id_tiempo`)));

